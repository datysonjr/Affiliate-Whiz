"""
data.models.experiments
~~~~~~~~~~~~~~~~~~~~~~~

Experiment model and CRUD operations for A/B testing in the OpenClaw system.

An experiment tracks a controlled test comparing two or more variants
(e.g. different headlines, CTA placements, or content formats) against a
target metric (CTR, conversion rate, revenue per visit).  The optimization
pipeline creates experiments, monitors them over a defined period, and
records the winning variant for future content decisions.

Design references:
    - ARCHITECTURE.md  Section 5 (Data Layer)
    - pipelines/optimization  (experiment lifecycle)
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass, field
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional

from src.core.logger import get_logger, log_event

logger = get_logger("data.models.experiments")

# SQL for the experiments table -- executed by Database.migrate() or
# directly in tests.
CREATE_TABLE_SQL = """
CREATE TABLE IF NOT EXISTS experiments (
    id           INTEGER PRIMARY KEY AUTOINCREMENT,
    name         TEXT    NOT NULL UNIQUE,
    variants     TEXT    NOT NULL DEFAULT '[]',
    metric       TEXT    NOT NULL DEFAULT 'ctr',
    start_date   TEXT,
    end_date     TEXT,
    winner       TEXT,
    status       TEXT    NOT NULL DEFAULT 'draft',
    created_at   TEXT    NOT NULL DEFAULT (datetime('now')),
    updated_at   TEXT    NOT NULL DEFAULT (datetime('now'))
);

CREATE INDEX IF NOT EXISTS idx_experiments_status ON experiments(status);
CREATE INDEX IF NOT EXISTS idx_experiments_metric ON experiments(metric);
"""


@dataclass
class Variant:
    """A single variant within an experiment.

    Attributes
    ----------
    name:
        Short identifier for the variant (e.g. ``"control"``, ``"variant_a"``).
    description:
        Human-readable description of what this variant changes.
    traffic_pct:
        Percentage of traffic allocated to this variant (0.0 -- 1.0).
    impressions:
        Number of impressions (views) this variant has received.
    conversions:
        Number of conversions attributed to this variant.
    value:
        Total monetary value generated by this variant.
    """

    name: str = ""
    description: str = ""
    traffic_pct: float = 0.5
    impressions: int = 0
    conversions: int = 0
    value: float = 0.0

    @property
    def conversion_rate(self) -> float:
        """Return the conversion rate as a decimal.

        Returns ``0.0`` when no impressions have been recorded.
        """
        if self.impressions <= 0:
            return 0.0
        return self.conversions / self.impressions

    def to_dict(self) -> Dict[str, Any]:
        """Serialize the variant to a plain dictionary."""
        return {
            "name": self.name,
            "description": self.description,
            "traffic_pct": self.traffic_pct,
            "impressions": self.impressions,
            "conversions": self.conversions,
            "value": self.value,
            "conversion_rate": round(self.conversion_rate, 6),
        }


@dataclass
class Experiment:
    """Represents a single A/B test experiment.

    Attributes
    ----------
    id:
        Auto-incremented primary key (``None`` before first save).
    name:
        Unique human-readable experiment name
        (e.g. ``"headline-test-standing-desks"``).
    variants:
        List of :class:`Variant` objects defining each test arm.
    metric:
        The primary metric being optimized -- ``ctr``, ``conversion_rate``,
        ``revenue_per_visit``, or ``bounce_rate``.
    start_date:
        UTC timestamp when the experiment started collecting data.
    end_date:
        UTC timestamp when the experiment was concluded.
    winner:
        Name of the winning variant (``None`` until the experiment concludes).
    status:
        Experiment lifecycle -- ``draft``, ``running``, ``paused``,
        ``completed``, ``cancelled``.
    created_at:
        UTC timestamp when the record was created.
    updated_at:
        UTC timestamp of the most recent update.
    """

    id: Optional[int] = None
    name: str = ""
    variants: List[Variant] = field(default_factory=list)
    metric: str = "ctr"
    start_date: Optional[str] = None
    end_date: Optional[str] = None
    winner: Optional[str] = None
    status: str = "draft"
    created_at: Optional[str] = None
    updated_at: Optional[str] = None

    # ------------------------------------------------------------------
    # Convenience helpers
    # ------------------------------------------------------------------

    @property
    def is_running(self) -> bool:
        """Return ``True`` if the experiment is actively collecting data."""
        return self.status == "running"

    @property
    def is_concluded(self) -> bool:
        """Return ``True`` if the experiment has reached a terminal state."""
        return self.status in ("completed", "cancelled")

    @property
    def total_impressions(self) -> int:
        """Return the sum of impressions across all variants."""
        return sum(v.impressions for v in self.variants)

    @property
    def duration_days(self) -> Optional[float]:
        """Return the experiment duration in days, or ``None`` if not started.

        If the experiment is still running, duration is measured from
        ``start_date`` to now.
        """
        if not self.start_date:
            return None
        start = datetime.fromisoformat(self.start_date)
        if self.end_date:
            end = datetime.fromisoformat(self.end_date)
        else:
            end = datetime.now(timezone.utc)
        delta = end - start
        return delta.total_seconds() / 86400.0

    def determine_winner(self) -> Optional[str]:
        """Determine the winning variant based on the target metric.

        Compares variants by their conversion rate (the universal proxy
        for most metrics) and returns the name of the best performer.
        Updates :attr:`winner` on the instance.

        Returns
        -------
        str or None
            Name of the winning variant, or ``None`` if no variants
            have data.
        """
        if not self.variants:
            return None

        best: Optional[Variant] = None
        for variant in self.variants:
            if variant.impressions <= 0:
                continue
            if best is None or variant.conversion_rate > best.conversion_rate:
                best = variant

        if best is not None:
            self.winner = best.name
        return self.winner

    def to_dict(self) -> Dict[str, Any]:
        """Serialize the experiment to a plain dictionary."""
        data = asdict(self)
        data["variants"] = [v.to_dict() for v in self.variants]
        return data


class ExperimentRepository:
    """CRUD operations for :class:`Experiment` records.

    Variants are serialized as a JSON array in the ``variants`` column.

    Parameters
    ----------
    db:
        An initialised ``Database`` instance.
    """

    def __init__(self, db: Any) -> None:
        self._db = db

    def ensure_table(self) -> None:
        """Create the ``experiments`` table and indexes if they do not exist."""
        self._db.execute(CREATE_TABLE_SQL)

    # ------------------------------------------------------------------
    # Create
    # ------------------------------------------------------------------

    def create(self, experiment: Experiment) -> Experiment:
        """Insert a new experiment and return it with its generated ``id``.

        Parameters
        ----------
        experiment:
            An ``Experiment`` instance.  The ``id`` field is ignored.

        Returns
        -------
        Experiment
            The same instance with ``id``, ``created_at``, and ``updated_at``
            populated.

        Raises
        ------
        DatabaseError
            If the insert fails (e.g. duplicate name).
        """
        now = datetime.now(timezone.utc).isoformat()
        variants_json = json.dumps([v.to_dict() for v in experiment.variants])

        cursor = self._db.execute(
            """
            INSERT INTO experiments
                (name, variants, metric, start_date, end_date, winner,
                 status, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                experiment.name,
                variants_json,
                experiment.metric,
                experiment.start_date,
                experiment.end_date,
                experiment.winner,
                experiment.status,
                now,
                now,
            ),
        )

        experiment.id = cursor.lastrowid
        experiment.created_at = now
        experiment.updated_at = now
        log_event(logger, "experiment.created", id=experiment.id, name=experiment.name)
        return experiment

    # ------------------------------------------------------------------
    # Read
    # ------------------------------------------------------------------

    def get_by_id(self, experiment_id: int) -> Optional[Experiment]:
        """Fetch a single experiment by primary key.

        Returns
        -------
        Experiment or None
            The experiment if found, otherwise ``None``.
        """
        row = self._db.fetch_one(
            "SELECT * FROM experiments WHERE id = ?", (experiment_id,)
        )
        if row is None:
            return None
        return self._row_to_experiment(row)

    def get_by_name(self, name: str) -> Optional[Experiment]:
        """Fetch a single experiment by its unique name.

        Returns
        -------
        Experiment or None
            The experiment if found, otherwise ``None``.
        """
        row = self._db.fetch_one(
            "SELECT * FROM experiments WHERE name = ?", (name,)
        )
        if row is None:
            return None
        return self._row_to_experiment(row)

    def list_all(
        self,
        *,
        status: Optional[str] = None,
        metric: Optional[str] = None,
        limit: int = 100,
        offset: int = 0,
    ) -> List[Experiment]:
        """Return experiments matching optional filters.

        Parameters
        ----------
        status:
            Filter by status (e.g. ``"running"``).
        metric:
            Filter by target metric (e.g. ``"ctr"``).
        limit:
            Maximum rows to return.
        offset:
            Number of rows to skip (for pagination).

        Returns
        -------
        list[Experiment]
            Matching experiments ordered by ``id`` descending.
        """
        clauses: List[str] = []
        params: List[Any] = []

        if status is not None:
            clauses.append("status = ?")
            params.append(status)
        if metric is not None:
            clauses.append("metric = ?")
            params.append(metric)

        where = f"WHERE {' AND '.join(clauses)}" if clauses else ""
        sql = f"SELECT * FROM experiments {where} ORDER BY id DESC LIMIT ? OFFSET ?"
        params.extend([limit, offset])

        rows = self._db.fetch_all(sql, params)
        return [self._row_to_experiment(r) for r in rows]

    def list_running(self) -> List[Experiment]:
        """Return all currently running experiments.

        Returns
        -------
        list[Experiment]
            Active experiments ordered by start date.
        """
        rows = self._db.fetch_all(
            "SELECT * FROM experiments WHERE status = 'running' ORDER BY start_date ASC"
        )
        return [self._row_to_experiment(r) for r in rows]

    # ------------------------------------------------------------------
    # Update
    # ------------------------------------------------------------------

    def update(self, experiment: Experiment) -> Experiment:
        """Persist changes to an existing experiment.

        Parameters
        ----------
        experiment:
            An ``Experiment`` with a valid ``id``.

        Returns
        -------
        Experiment
            The updated experiment with a refreshed ``updated_at`` timestamp.
        """
        now = datetime.now(timezone.utc).isoformat()
        variants_json = json.dumps([v.to_dict() for v in experiment.variants])

        self._db.execute(
            """
            UPDATE experiments
               SET name = ?, variants = ?, metric = ?, start_date = ?,
                   end_date = ?, winner = ?, status = ?, updated_at = ?
             WHERE id = ?
            """,
            (
                experiment.name,
                variants_json,
                experiment.metric,
                experiment.start_date,
                experiment.end_date,
                experiment.winner,
                experiment.status,
                now,
                experiment.id,
            ),
        )

        experiment.updated_at = now
        log_event(logger, "experiment.updated", id=experiment.id)
        return experiment

    def start(self, experiment_id: int) -> Optional[Experiment]:
        """Start an experiment by setting its status to ``running``.

        Parameters
        ----------
        experiment_id:
            The experiment's ``id``.

        Returns
        -------
        Experiment or None
            The updated experiment, or ``None`` if not found.
        """
        now = datetime.now(timezone.utc).isoformat()
        self._db.execute(
            """
            UPDATE experiments
               SET status = 'running', start_date = ?, updated_at = ?
             WHERE id = ?
            """,
            (now, now, experiment_id),
        )
        log_event(logger, "experiment.started", id=experiment_id)
        return self.get_by_id(experiment_id)

    def complete(self, experiment_id: int, winner: Optional[str] = None) -> Optional[Experiment]:
        """Mark an experiment as completed and record the winner.

        Parameters
        ----------
        experiment_id:
            The experiment's ``id``.
        winner:
            Name of the winning variant.  If ``None``, the winner is
            auto-determined from variant metrics.

        Returns
        -------
        Experiment or None
            The completed experiment, or ``None`` if not found.
        """
        experiment = self.get_by_id(experiment_id)
        if experiment is None:
            return None

        if winner is None:
            winner = experiment.determine_winner()

        now = datetime.now(timezone.utc).isoformat()
        self._db.execute(
            """
            UPDATE experiments
               SET status = 'completed', end_date = ?, winner = ?, updated_at = ?
             WHERE id = ?
            """,
            (now, winner, now, experiment_id),
        )
        log_event(
            logger, "experiment.completed",
            id=experiment_id, winner=winner,
        )
        return self.get_by_id(experiment_id)

    def cancel(self, experiment_id: int) -> bool:
        """Cancel a running experiment.

        Parameters
        ----------
        experiment_id:
            The experiment's ``id``.

        Returns
        -------
        bool
            ``True`` if the experiment was found and cancelled.
        """
        now = datetime.now(timezone.utc).isoformat()
        cursor = self._db.execute(
            """
            UPDATE experiments
               SET status = 'cancelled', end_date = ?, updated_at = ?
             WHERE id = ?
            """,
            (now, now, experiment_id),
        )
        if cursor.rowcount > 0:
            log_event(logger, "experiment.cancelled", id=experiment_id)
            return True
        return False

    # ------------------------------------------------------------------
    # Delete
    # ------------------------------------------------------------------

    def delete(self, experiment_id: int) -> bool:
        """Delete an experiment by primary key.

        Parameters
        ----------
        experiment_id:
            The experiment's ``id``.

        Returns
        -------
        bool
            ``True`` if a row was deleted, ``False`` if no such experiment.
        """
        cursor = self._db.execute(
            "DELETE FROM experiments WHERE id = ?", (experiment_id,)
        )
        deleted = cursor.rowcount > 0
        if deleted:
            log_event(logger, "experiment.deleted", id=experiment_id)
        return deleted

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _parse_variants(raw: str) -> List[Variant]:
        """Parse a JSON string into a list of :class:`Variant` objects."""
        try:
            items = json.loads(raw) if raw else []
        except (json.JSONDecodeError, TypeError):
            return []

        variants: List[Variant] = []
        for item in items:
            if isinstance(item, dict):
                variants.append(
                    Variant(
                        name=item.get("name", ""),
                        description=item.get("description", ""),
                        traffic_pct=item.get("traffic_pct", 0.5),
                        impressions=item.get("impressions", 0),
                        conversions=item.get("conversions", 0),
                        value=item.get("value", 0.0),
                    )
                )
        return variants

    @classmethod
    def _row_to_experiment(cls, row: Dict[str, Any]) -> Experiment:
        """Convert a database row dict into an ``Experiment`` dataclass."""
        variants = cls._parse_variants(row.get("variants", "[]"))

        return Experiment(
            id=row["id"],
            name=row["name"],
            variants=variants,
            metric=row.get("metric", "ctr"),
            start_date=row.get("start_date"),
            end_date=row.get("end_date"),
            winner=row.get("winner"),
            status=row.get("status", "draft"),
            created_at=row.get("created_at"),
            updated_at=row.get("updated_at"),
        )
